public class Point {
    // Variables for point x and y
    private double x;
    private double y;

    // Parameterized constructor to set values to x and y point
    public Point(double x, double y){
        this.x = x;
        this.y = y;
    }

    // Get point x
    public double getX(){
        return x;
    }

    // Get point y
    public double getY(){
        return y;
    }
}
